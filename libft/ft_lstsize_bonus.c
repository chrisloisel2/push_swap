#include "libft.h"

int		ft_lstsize(t_list *lst)
{
	int		i;
	t_list	*t;

	t = lst;
	i = 0;
	if (lst != NULL)
	{
		while (t->next != NULL)
		{
			t = t->next;
			i++;
		}
		i++;
	}
	return (i);
}
